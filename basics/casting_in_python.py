#You can cast variables by using this functions:
# int(), bool(), str(), float()

print(int(3.4242)) # float -> int
print(float("6.232")) # str -> float

thisIsString = "4004"
print(int(thisIsString)) # str -> int
print(type(int(thisIsString)))

print(bool(1)) # True - 1 or other value # int -> bool
print(bool(0)) # False - 0

print(str(424))
print(type(str(424))) # int -> str

